    private void savebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebtnActionPerformed
        // TODO add your handling code here:
        Object part = evt.getSource();
        if(part == savebtn)
        {
            savebtn.setEnabled(true);
        }      
        if(bCat1.getSelectedIndex()<=0) return;
        try
        {
            String userName = "root";
            String password = "";
            String url      = "jdbc:mysql://localhost:3306/personal_Book_Manager";
            Class.forName("com.mysql.jdbc.Driver");
            Connection con  = DriverManager.getConnection(url, userName, password);
            Statement st    = con.createStatement();
            
            String name     =  namet.getText();
            String bname    =  bCat1.getSelectedItem().toString();
            String author   =  authort.getText();
            String email    =  e_mail.getText();
            String phn      =  phn_no.getText();
            String idate    =  ((JTextField)issuedate.getDateEditor().getUiComponent()).getText();
            String bdate    =  ((JTextField)returndate.getDateEditor().getUiComponent()).getText();
            
            String insertto = "";
            insertto = "INSERT INTO borrowers_list (Name, Book_Name, Author,E_Mail, Phone_No, Issue_Date, Back_Date)"
                    + "values('"+name+"','"+bname+"','"+author+"','"+email+"','"+phn+"','"+idate+"','"+bdate+"')";
            st.execute(insertto);
            JOptionPane.showMessageDialog(null, "Successfully inserted");
            
            ShowAllBorrowers();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Got exception");
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_savebtnActionPerformed

