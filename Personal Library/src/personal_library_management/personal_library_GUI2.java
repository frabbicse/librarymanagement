    private void bCatItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_bCatItemStateChanged
        try {
            // TODO add your handling code here:
            String userName = "root";
            String password = "";
            String url      = "jdbc:mysql://localhost:3306/personal_Book_Manager";
            Class.forName("com.mysql.jdbc.Driver");
            Connection con  = DriverManager.getConnection(url, userName, password);
            Statement st    = con.createStatement();
            String cat = bCat.getSelectedItem().toString();
            String q = "select Book_Name,Writer_Name from book_lists where Catagory = '"+cat+"'";
            String model ="--Select--";
            ba = new TreeMap<String, String>();
            ResultSet res = st.executeQuery(q);
            while(res.next())
            {
                model+=","+res.getString(1);
                ba.put(res.getString(1), res.getString(2));
            }
            bCat1.setModel(new DefaultComboBoxModel(model.split(",")));
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(personal_library_GUI2.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(personal_library_GUI2.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bCatItemStateChanged

